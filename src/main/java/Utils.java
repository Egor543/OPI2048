public class Utils {

    public static int add (int a, int b)
    {return a+b;}
    public static int div (int a, int b)
    {return a/b;}
    public static int con (int a, int b)
    {return a*b;}
    public static int det (int a, int b)
    {return a-b;}
    public static int add1 (int a, int b, int c)
    {return a+b+c;}
    public static int div1 (int a, int b, int c)
    {return a/(b+c);}
    public static int con1 (int a, int b, int c)
    {return a*b*c;}
    public static int det1 (int a, int b, int c)
    {return a-b-c;}
    public static int con2 (int a, int b, int c)
    {return a*(b+c);}
    public static int con3 (int a, int b, int c)
    {return (a+b)*c;}
    public static int con4 (int a, int b, int c)
    {return (a+c)*b;}

}
